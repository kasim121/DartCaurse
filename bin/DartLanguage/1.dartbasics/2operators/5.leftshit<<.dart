void main() {
  int num = 5; // Binary representation: 0000 0101
  
  // Left shift by 2 positions
  int result = num << 2; // Binary representation: 0001 0100
  
  print(result); // Output: 20
}
/*
Decimal: 5
Binary:  0000 0101

After left shifting by 2 positions:
Binary:  0001 0100

Decimal: 20
*/