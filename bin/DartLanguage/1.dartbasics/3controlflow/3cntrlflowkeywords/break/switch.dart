void main() {
  //II. Exiting a Switch Statement:

  String fruit = 'apple';

  switch (fruit) {
    case 'banana':
      print('You selected a banana.');
      break;
    case 'apple':
      print('You selected an apple.');
      break;
    default:
      print('You selected a fruit other than banana or apple.');
      break;
  }
}
