void main() {
//   Combining Lists with +:
// You can combine two lists into one using the + operator.
// This operator concatenates the elements of the two lists, creating a new list.
  List<int> list1 = [1, 2, 3];
  List<int> list2 = [4, 5, 6];
  List<int> combinedList = list1 + list2;
  print(combinedList); // Output: [1, 2, 3, 4, 5, 6]
}
